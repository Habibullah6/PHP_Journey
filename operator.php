<?php


// Initialize variable
$number = 10;

// Post-increment
echo $number++; // Outputs: 10 (original value)
echo "<br>";
echo $number;   // Outputs: 11 (incremented value)

// Pre-increment
echo ++$number; // Outputs: 12 (incremented value)
echo "<br>";

// Post-decrement
echo $number--; // Outputs: 12 (original value)
echo "<br>";
echo $number;   // Outputs: 11 (decremented value)

// Pre-decrement
echo --$number; // Outputs: 10 (decremented value)

