<?php
$decimal = 15;

// Convert decimal to binary
$binary = decbin($decimal);
echo "Decimal $decimal to Binary: $binary\n"; // Output: 1111

// Convert decimal to octal
$octal = decoct($decimal);
echo "Decimal $decimal to Octal: $octal\n"; // Output: 17

// Convert decimal to hexadecimal
$hexadecimal = dechex($decimal);
echo "Decimal $decimal to Hexadecimal: $hexadecimal\n"; // Output: f






